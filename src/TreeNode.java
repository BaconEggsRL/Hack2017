
public class TreeNode {
	
	private int count;
	private Character character;
	

	public TreeNode() {
		
		count = 0;
		character = null;
			
	}
	
	public void setCount(int value) {
		
		this.count = value;
		
	}
	
	public void setCharacter(char character) {
		
		this.character = character;
		
	}

}
